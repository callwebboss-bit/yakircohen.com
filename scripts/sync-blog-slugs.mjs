/**
 * Extract blog post slugs from lib/data/blog.ts into lib/data/blog-slugs.ts.
 * Keeps sitemap.xml lightweight (no full blog content import).
 *
 * Usage: node scripts/sync-blog-slugs.mjs
 */
import fs from "node:fs";
import path from "node:path";

const ROOT = process.cwd();
const BLOG_FILE = path.join(ROOT, "lib", "data", "blog.ts");
const SLUGS_FILE = path.join(ROOT, "lib", "data", "blog-slugs.ts");

const content = fs.readFileSync(BLOG_FILE, "utf8");
const postsStart = content.indexOf("export const BLOG_POSTS");
if (postsStart === -1) {
  console.error("Could not find BLOG_POSTS in blog.ts");
  process.exit(1);
}

const postsSection = content.slice(postsStart);
const slugs = [...postsSection.matchAll(/^\s+slug:\s*"([^"]+)"/gm)].map(
  (m) => m[1],
);

if (slugs.length === 0) {
  console.error("No slugs found in BLOG_POSTS");
  process.exit(1);
}

const unique = new Set(slugs);
if (unique.size !== slugs.length) {
  console.error("Duplicate slugs detected in blog.ts");
  process.exit(1);
}

const out = `/** AUTO-GENERATED by scripts/sync-blog-slugs.mjs — do not edit manually */
export const BLOG_SLUGS = ${JSON.stringify(slugs, null, 2)} as const;

export type BlogPostSlug = (typeof BLOG_SLUGS)[number];

/** Lightweight slug list for sitemap and generateStaticParams */
export function getAllBlogSlugs(): readonly BlogPostSlug[] {
  return BLOG_SLUGS;
}
`;

fs.writeFileSync(SLUGS_FILE, out, "utf8");
console.log(`Wrote ${slugs.length} slugs to lib/data/blog-slugs.ts`);
