import { readFileSync, writeFileSync, mkdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { tagTitle, playlistsForTags } from "./portfolio-tag-rules.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

const DEFAULT_SOURCE = "d:\\active_portfolio.txt";
const sourcePath = process.argv[2] ?? DEFAULT_SOURCE;

const LINE_RE = /^(.+?)\s*->\s*(https?:\/\/\S+)\s*$/;

function parseYouTubeId(url) {
  try {
    const u = new URL(url.trim());
    if (u.hostname.includes("youtu.be")) {
      return u.pathname.replace(/^\//, "").split("/")[0] ?? null;
    }
    const v = u.searchParams.get("v");
    if (v) return v;
    const embed = u.pathname.match(/\/embed\/([^/?]+)/);
    return embed?.[1] ?? null;
  } catch {
    return null;
  }
}

function parseFile(text) {
  const entries = [];
  const skipped = [];
  for (const line of text.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const m = trimmed.match(LINE_RE);
    if (!m) {
      skipped.push({ line: trimmed, reason: "format" });
      continue;
    }
    const title = m[1].trim();
    const videoId = parseYouTubeId(m[2]);
    if (!videoId || videoId.length !== 11) {
      skipped.push({ line: trimmed, reason: "invalid-id" });
      continue;
    }
    entries.push({ title, videoId, youtubeUrl: m[2].trim() });
  }
  return { entries, skipped };
}

function buildCatalog(entries) {
  const seen = new Map();
  const duplicates = [];
  const videos = [];

  for (const entry of entries) {
    if (seen.has(entry.videoId)) {
      duplicates.push(entry.videoId);
      continue;
    }
    seen.set(entry.videoId, true);
    const tags = tagTitle(entry.title);
    const services = playlistsForTags(tags);
    videos.push({
      videoId: entry.videoId,
      title: entry.title,
      youtubeUrl: entry.youtubeUrl,
      tags,
      services,
    });
  }

  return { videos, duplicates };
}

function emitTypeScript(videos) {
  return [
    "/** AUTO-GENERATED by scripts/import-portfolio.mjs -- do not edit */",
    'import type { PortfolioVideo } from "./video-catalog";',
    "",
    `export const PORTFOLIO_VIDEO_CATALOG: readonly PortfolioVideo[] = ${JSON.stringify(videos, null, 2)} as const;`,
    "",
    `export const PORTFOLIO_CATALOG_COUNT = ${videos.length} as const;`,
    "",
  ].join("\n");
}

function main() {
  let text;
  try {
    text = readFileSync(sourcePath, "utf8");
  } catch (e) {
    console.error(`Cannot read ${sourcePath}:`, e.message);
    process.exit(1);
  }

  const { entries, skipped } = parseFile(text);
  const { videos, duplicates } = buildCatalog(entries);

  const outDir = join(ROOT, "lib", "data");
  mkdirSync(outDir, { recursive: true });
  const outFile = join(outDir, "video-catalog.generated.ts");
  writeFileSync(outFile, emitTypeScript(videos), "utf8");

  const tagCounts = {};
  for (const v of videos) {
    for (const t of v.tags) {
      tagCounts[t] = (tagCounts[t] ?? 0) + 1;
    }
  }

  const report = {
    source: sourcePath,
    parsed: entries.length,
    catalog: videos.length,
    skipped: skipped.length,
    duplicates: duplicates.length,
    tagCounts,
  };

  writeFileSync(
    join(ROOT, "portfolio-import-report.json"),
    JSON.stringify({ ...report, skippedSample: skipped.slice(0, 20) }, null, 2),
    "utf8",
  );

  console.log(`Wrote ${videos.length} videos → ${outFile}`);
  console.log(JSON.stringify(report, null, 2));
}

main();
