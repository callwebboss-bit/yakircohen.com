function allRegistryServices(): ServiceEntity[] {
  return [
    ...Object.values(STUDIO_SERVICES),
    ...Object.values(VOICEOVER_SERVICES),
    ...Object.values(EVENTS_SERVICES),
    ...Object.values(VIDEO_SERVICES),
    ...Object.values(PHOTOGRAPHY_SERVICES),
  ];
}

export function getRelatedServiceCallout(
  relatedServiceSlug: string,
): RelatedServiceCallout | null {
  const fromRegistry = allRegistryServices().find(
    (s) => s.slug === relatedServiceSlug,
  );
  if (fromRegistry) {
    return {
      href: `/${fromRegistry.slug}`,
      title: fromRegistry.title,
      subtitle: fromRegistry.subtitle,
      whatsappText: fromRegistry.whatsappText,
      utmCampaign: fromRegistry.utmCampaign,
    };
  }
  return PODCAST_SERVICE_LOOKUP[relatedServiceSlug] ?? null;
}

